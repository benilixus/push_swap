#include "checker.h"

int	is_digit(char c)
{
	return (c >= 48 && c <= 57);
}

long	ft_atol(const char *n)
{
	int		i;
	int		s;
	long	r;

	i = 0;
	s = 1;
	r = 0;
	while (n[i] == 32 || (n[i] >= 9 && n[i] <= 13))
		i++;
	if (n[i] == '-' || n[i] == '+')
	{
		if (n[i] == '-')
			s *= -1;
		i++;
	}
	while (is_digit(n[i]))
	{
		if ((s == 1 && r > 2147483647) || (s == -1 && r > 2147483648))
			return (9999999999);
		r = r * 10 + (n[i] - 48);
		i++;
	}
	return (r * s);
}
